export function generateMonitoringData(points: number = 100): MonitoringData[] {
  const data: MonitoringData[] = [];
  let cavitationVolume = 5;
  let pressureFluctuation = 30;
  let erosionRate = 0.1;
  let thrustFluctuation = 2;

  for (let i = 0; i < points; i++) {
    cavitationVolume += (Math.random() - 0.45) * 1.5;
    cavitationVolume = Math.max(0.5, Math.min(25, cavitationVolume));

    pressureFluctuation += (Math.random() - 0.48) * 5;
    pressureFluctuation = Math.max(10, Math.min(100, pressureFluctuation));

    erosionRate += (Math.random() - 0.45) * 0.02;
    erosionRate = Math.max(0.01, Math.min(0.5, erosionRate));

    thrustFluctuation += (Math.random() - 0.48) * 0.5;
    thrustFluctuation = Math.max(0.5, Math.min(10, thrustFluctuation));

    data.push({
      timestamp: i * 10,
      cavitationVolume: parseFloat(cavitationVolume.toFixed(2)),
      pressureFluctuation: parseFloat(pressureFluctuation.toFixed(1)),
      erosionRate: parseFloat(erosionRate.toFixed(3)),
      thrustFluctuation: parseFloat(thrustFluctuation.toFixed(2)),
      minPressure: parseFloat((2 + Math.random() * 1.5).toFixed(2)),
      maxShearStress: parseFloat((100 + Math.random() * 80).toFixed(1)),
    });
  }

  return data;
}

